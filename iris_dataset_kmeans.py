import pandas as pd
import matplotlib.pyplot as plt
import numpy as np
import seaborn as sns

df = pd.read_csv("/content/iris.csv")
df.head()

sns.scatterplot(df , x= 'petal.length', y= 'petal.width', hue='variety')
plt.show()

from sklearn.cluster import KMeans
X = df.iloc[:,0:4] # Quiero ver si hacen los mismos clases que Species

wcss = []
for i in range (1,11):
  kmeans = KMeans(n_clusters=i ,
                  init = "k-means++",
                  max_iter = 300,
                  n_init= 10 ,
                  random_state=0)
  kmeans.fit(X)
  wcss.append(kmeans.inertia_)

plt.plot(range(1,11),wcss)
plt.title("Método del codo")
plt.xlabel("Número de Clusters")
plt.ylabel("WCSS(k)")
plt.xticks(np.arange(1, 11, step=1)) # para tener el número de clusters
plt.show()

kmeans = KMeans( n_clusters = 3 , init = "k-means++", max_iter = 300, n_init =10, random_state=0 )
y_kmeans = kmeans.fit_predict(X)

Rkmeans = X
Rkmeans['Cluster'] = y_kmeans
Rkmeans.head()

print(kmeans.cluster_centers_)

sns.scatterplot(Rkmeans , x = 'petal.length', y='petal.width', hue='Cluster', palette = 'Set2')
clusterplot = plt.scatter(kmeans.cluster_centers_[:,2], kmeans.cluster_centers_[:,3],
            s = 300, c = "Yellow", label ="Baricentros") # Las posiciones del cluster son por la posición del feature
plt.show()

fig, axes = plt.subplots(nrows=1, ncols=2, figsize=(12, 6))
sns.scatterplot(df , x= 'petal.length', y= 'petal.width', hue='variety' , ax=axes[0])
axes[0].set_title('Gráfico con etiquetas IrisDataset')

sns.scatterplot(Rkmeans , x = 'petal.length', y='petal.width', hue='Cluster', palette = 'Set2', ax = axes[1])
plt.scatter(kmeans.cluster_centers_[:,2], kmeans.cluster_centers_[:,3],
            s = 300, c = "Yellow", label ="Baricentros")
axes[1].set_title('Gráfico Utilizando KMeans')


plt.tight_layout()
plt.show()